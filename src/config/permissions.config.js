export const getPermissionsByRole = role => {
  return PERMISSIONS[role] || PERMISSIONS.viewer;
};

export const hasPermission = (role, permission) => {
  const rolePermissions = getPermissionsByRole(role);
  return rolePermissions[permission] || false;
};

export const getGrantedPermissions = role => {
  const rolePermissions = getPermissionsByRole(role);
  return Object.keys(rolePermissions).filter(key => rolePermissions[key] === true);
};

export const getDeniedPermissions = role => {
  const rolePermissions = getPermissionsByRole(role);
  return Object.keys(rolePermissions).filter(key => rolePermissions[key] === false);
};

export const compareRoles = (role1, role2) => {
  const perms1 = getPermissionsByRole(role1);
  const perms2 = getPermissionsByRole(role2);
  const differences = {};
  const allKeys = new Set([...Object.keys(perms1), ...Object.keys(perms2)]);
  allKeys.forEach(key => {
    if (perms1[key] !== perms2[key]) {
      differences[key] = { [role1]: perms1[key], [role2]: perms2[key] };
    }
  });
  return differences;
};

export default {
  PERMISSIONS,
  getPermissionsByRole,
  hasPermission,
  getGrantedPermissions,
  getDeniedPermissions,
  compareRoles,
};